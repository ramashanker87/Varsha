package com.rama.app;

public class Calculator {
    public int add(int a,int b) {
        return a + b;
    }
    public double mul(int a,int b) {
        return a * b;
    }
    public float sub(int a,int b) {
        return a - b;
    }
    public double Division(int a,int b) {
        return a/b;
    }
}
